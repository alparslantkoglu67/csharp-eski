    public partial class Form1 : Form
    {
        public Form1()
        {
            InitializeComponent();
        }

        private void button1_Click(object sender, EventArgs e)
        {
            double sayi1, sayi2, toplam, fark, carpim, bolum;

            sayi1 = Convert.ToInt16(textBox1.Text);
            sayi2 = Convert.ToInt16(textBox2.Text);
            toplam = sayi1 + sayi2;
            fark = sayi1 - sayi2;
            carpim = sayi1 * sayi2;
            bolum = sayi1 / sayi2;

            textBox3.Text = Convert.ToString(toplam);
            textBox4.Text = Convert.ToString(fark);
            textBox5.Text = Convert.ToString(carpim);
            textBox6.Text = Convert.ToString(bolum);


        }
    }
