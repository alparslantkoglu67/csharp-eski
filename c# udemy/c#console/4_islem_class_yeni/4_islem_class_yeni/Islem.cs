using System;
using System.Collections.Generic;
using System.Text;

namespace _4_islem_class_yeni
{
    class Islem
    {
        public double Topla(double s1, double s2)
        {

            double s3 = s1 + s2;
            return s3;


        }
        public double Cikar(double s1, double s2)
        {

            double s3 = s1 - s2;
            return s3;


        }
        public double Carp(double s1, double s2)
        {

            double s3 = s1 * s2;
            return s3;


        }
        public double Bol(double s1, double s2)
        {

            double s3 = s1 / s2;
            return s3;


        }
        public double Modal(double s1, double s2)
        {

            double s3 = s1 % s2;
            return s3;

            

        }
        
    }


}

